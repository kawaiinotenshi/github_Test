/**
 * 简单的加法函数
 * @param {number} a - 第一个数字
 * @param {number} b - 第二个数字
 * @returns {number} 两数之和
 */
function add(a, b) {
  return a + b;
}

/**
 * 简单的乘法函数
 * @param {number} a - 第一个数字
 * @param {number} b - 第二个数字
 * @returns {number} 两数之积
 */
function multiply(a, b) {
  return a * b;
}

module.exports = {
  add,
  multiply
};